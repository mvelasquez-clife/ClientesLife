        async function ValidarInfoCliente(event) {
            document.getElementById('rg-codigo').value = '';
            document.getElementById('rg-nmcomercial').value = '';
            document.getElementById('rg-rzsocial').value = '';
            document.getElementById('rg-email').value = '';
            document.getElementById('rg-telefono').value = '';
            document.getElementById('rg-clave').value = '';
            document.getElementById('rg-rclave').value = '';
            let p = {
                rucdni: document.getElementById('form-ruc').value
            };
            try {
                let result = await $.ajax({
                    url: '{{ url("api/app-life/busca") }}',
                    type: 'post',
                    data: p,
                    dataType: 'json'
                });
                if(result.status) {
                    $('#form-registra-cliente').fadeIn(150);
                    let data = result.data.cliente;
                    document.getElementById('rg-codigo').value = p.rucdni;
                    document.getElementById('rg-rzsocial').value = data.rsocial;
                    document.getElementById('rg-nmcomercial').value = data.ncomercial;
                    document.getElementById('rg-email').value = document.getElementById('form-email').value;
                    document.getElementById('rg-telefono').value = data.telefono;
                }
            }
            catch(exception) {
                alert('Ocurrió un error...');
                console.log(exception);
            }
        }
        async function GuardarDatosCliente(event) {
            event.preventDefault();
            event.preventDefault();
            let password = document.getElementById('rg-clave').value;
            let rpassword = document.getElementById('rg-rclave').value;
            if(password == rpassword) {
                try {
                    let result = await $.ajax({
                        url: '{{ url("api/app-life/registro") }}',
                        type: 'POST',
                        data: {
                            codigo: document.getElementById('rg-codigo').value,
                            ncomercial: document.getElementById('rg-nmcomercial').value,
                            rsocial: document.getElementById('rg-rzsocial').value,
                            email: document.getElementById('rg-email').value,
                            telefono: document.getElementById('rg-telefono').value,
                            clave: document.getElementById('rg-clave').value
                        },
                        dataType: 'json'
                    });
                    if(result.status) {
                        $('#modal-registro').modal('hide');
                        alert('¡Cliente registrado! Ya puedes descargar la app e ingresar con tu RUC y contraseña');
                        document.getElementById('form-name').value = '';
                        document.getElementById('form-email').value = '';
                        document.getElementById('form-ruc').value = '';
                    }
                    else alert(result.message);
                }
                catch(exception) {
                    alert('Ocurrió un error...');
                    console.log(exception);
                }
            }
            else alert('Las claves ingresadas deben coincidir');
        }
        $('#modal-registro').on('show.bs.modal', ValidarInfoCliente);
        $('#modal-registro .btn-primary').on('click', GuardarDatosCliente);
